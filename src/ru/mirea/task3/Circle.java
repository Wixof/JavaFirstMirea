package ru.mirea.task3;

public class Circle {
    private float radius;
    private boolean filled;

    public Circle() {}

    public float getRadius() {
        return radius;
    }

    public boolean isFilled() {
        return filled;
    }

    public void setRadius(float radius) {
        this.radius = radius;
    }

    public void setFilled(boolean filled) {
        this.filled = filled;
    }
}
